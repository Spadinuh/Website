			<p class="discription">January to August I will be taking a 7 months gap semester because I am a project-driven person and I am interested by photojournalism, I’ve decided to take advantage of my trip to Europe by highlight an current problematic. From nomads, to troubadours and Bohemians, travelling and singing tales are an ancestral tradition. Street musicians are their proud descendants and Europe has a fruitful culture thanks to its size and accessible transportation. These days however it is becoming harder to earn a decent living as we no longer carry change and prefer downloading to your phone. As someone who is curious about meeting current street musicians I am interested in contrasting their realities with the ones in Charles Aznavour’s songs a few decades ago when there weren’t all those limitations. I am inspired by gonzo journalism (journalism that is more involved in the community and less from a third-person viewpoint). Therefore I will also busk to sustain myself for food and sleep. This is an example of a book cover of a book I’d like to write about that experience.</p>
